// Tanpa Ternary Operator
const nilai = 75;
let ucapan;

if ( nilai >= 75 ) { 
    ucapan = "Selamat Anda Lulus";
 } else { 
    ucapan = "Silahkan Coba Lagi!";
};
console.log(ucapan);


// Kode dengan Ternary Operator
const value = 80;
const kata = value >= 75 ? "Selamat anda Lulus" : "Silahkan Coba Lagi";
console.log(kata);
